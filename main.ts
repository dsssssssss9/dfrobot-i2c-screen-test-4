lcdDisplay.lcdInitIIC()
lcdDisplay.lcdClearAll()
lcdDisplay.lcdSetBgcolor(0xffffff)
basic.forever(function () {
    lcdDisplay.lcdDrawLine(1, 40, 120, 300, 120, 20, 0x007FFF)
    basic.pause(1000)
    lcdDisplay.lcdDrawRectangle(1, 10, 10, 300, 200, 15, 0xFF0000, lcdDisplay.DrawType.Fill, 0xFFFFFF, lcdDisplay.RectangleRound.NoneRound)
    basic.pause(1000)
    lcdDisplay.lcdDrawCircle(1, 160, 120, 120, 10, 0x00ff00, lcdDisplay.DrawType.Fill, 0xFFFFFF)
    basic.pause(1000)
    lcdDisplay.lcdDrawTriangle(1, 160, 0, 0, 240, 320, 240, 5, 0xff0080, lcdDisplay.DrawType.Fill, 0xFFFFFF)
    basic.pause(1000)
    lcdDisplay.lcdDeleteWidget(lcdDisplay.getLCDWidgetCategoryTwo(LCDWidgetCategoryTwo.Triangle), 1)
    basic.pause(1000)
    lcdDisplay.lcdDeleteWidget(lcdDisplay.getLCDWidgetCategoryTwo(LCDWidgetCategoryTwo.Circle), 1)
    basic.pause(1000)
    lcdDisplay.lcdDeleteWidget(lcdDisplay.getLCDWidgetCategoryTwo(LCDWidgetCategoryTwo.Rectangle), 1)
    basic.pause(1000)
    lcdDisplay.lcdDeleteWidget(lcdDisplay.getLCDWidgetCategoryTwo(LCDWidgetCategoryTwo.Line), 1)
    basic.pause(1000)
})
